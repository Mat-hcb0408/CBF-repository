package org.cbf.cbf.POJO;

//Essa classe é chamada de POJO (Plain Old Java Object), ou em português, Objeto Java Simples.
//
//POJOs são classes Java que têm a intenção de representar dados de forma simples, sem depender de funcionalidades complexas ou bibliotecas externas. Em geral, um POJO tem os seguintes padrões:
//
//Atributos privados (encapsulamento).
//
//Construtores para inicializar os objetos.
//
//Getters e setters (ou apenas getters, se não houver necessidade de alterar os dados após a inicialização).
//
//Nenhuma lógica de negócios (é usado mais para armazenar e transportar dados).
//
//No caso da sua classe Transacao, ela tem os atributos necessários para representar uma transação financeira e também métodos para acessar esses dados. Então, ela se encaixa bem no conceito de POJO.